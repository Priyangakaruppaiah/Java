//Java program to convert int type variables to long
import java.util.Scanner;
public class Main
{
	public static void main(String[] args) {
	    Scanner sc=new Scanner(System.in);
	    int n1=sc.nextInt();
	    int n2=sc.nextInt();
	    long n3=n1;
	    long n4=n2;
		System.out.println(n3);
		System.out.println(n4);
	}
}
